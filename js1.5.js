let eka, toka, kolmas;
eka = prompt("Syötä ensimmäinen kokonaisluku");
toka = prompt("Syötä toinen kokonaisluku");
kolmas = prompt("Syötä kolmas kokonaisluku");
alert("Lukujen summa on " + (parseInt(eka)+parseInt(toka)+parseInt(kolmas)) + ", tulo on "
    + eka*toka*kolmas + " ja keskiarvo on " + (parseInt(eka)+parseInt(toka)+parseInt(kolmas))/3);